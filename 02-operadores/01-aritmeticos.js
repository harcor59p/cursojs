let a = 5 ;
let b = 7 ;

console.log(a + b , 'adicion') ; // adicion
console.log(a - b , 'resta') ; // resta
console.log(a * b , 'multiplicación') ; // multiplicación
console.log(a / b , 'división') ; // división
console.log(a % b , 'módulo') ; // módulo
console.log(a ** b , 'potencia') ; // ptencia

// incrementar
 console.log(++a);
 console.log(a++);
 console.log(a);


// decrementar

console.log(--a);
console.log(a--);
console.log(a);


